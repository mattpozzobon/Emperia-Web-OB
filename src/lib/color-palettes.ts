// ─── Light Color Palette ─────────────────────────────────────────────────────
// Matches the client's decodeLightColor() in light-renderer.ts exactly:
//   r = 51 * (floor(idx / 36) % 6)
//   g = 51 * (floor(idx / 6) % 6)
//   b = 51 * (idx % 6)
// Valid indices: 1-215 (index 0 and ≥216 map to white in the client)

export function decodeLightColor(idx: number): [number, number, number] {
  if (idx <= 0 || idx >= 216) return [255, 255, 255];
  const r = 51 * (Math.floor(idx / 36) % 6);
  const g = 51 * (Math.floor(idx / 6) % 6);
  const b = 51 * (idx % 6);
  return [r, g, b];
}

/** Pre-built light palette: indices 0-215 (0 = white fallback). */
export const LIGHT_PALETTE: [number, number, number][] = Array.from(
  { length: 216 },
  (_, i) => decodeLightColor(i),
);

export function rgbToHex(r: number, g: number, b: number): string {
  return '#' + ((1 << 24) | (r << 16) | (g << 8) | b).toString(16).slice(1);
}

